package java12features.unicodecharacters;

public class Chess {

    public static void main(String[] args) {

        // In java 12 unicode characters representing chess game pawns are added

        System.out.println("Chess game black king: \u265A");
        System.out.println("Chess game black Queen: \u265B");

        System.out.println("Chess game White King: \u2654");
        System.out.println("Chess game White Queen: \u2655");
    }
}
